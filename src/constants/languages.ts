export function getFlagForLanguage(langName: string): string {
  const lang = LANGUAGES.find(
    (l) => l.name.toLowerCase() === langName.toLowerCase() || l.code.toLowerCase() === langName.toLowerCase()
  );
  return lang?.flag ?? '🌐';
}
